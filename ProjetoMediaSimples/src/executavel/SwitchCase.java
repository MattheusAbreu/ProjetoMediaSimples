package executavel;

public class SwitchCase {
	
	public static String verificarDia(int dia) {
		
		switch(dia) {
		
		case 1:
			return "Segunda-feira";
		case 2:
			return "Terca-Feira";
		case 3:
			return "Quarta-Feira";
		case 4:
			return "Quinta-Feira";
		case 5:
			return "Sexta-Feira";
		case 6:
			return "Sabado";
		case 7:
			return "Domingo";
		default :
			return "Valor invalido";
			
		
		
		
		}
		
		
		
		
	}
	
	

}
